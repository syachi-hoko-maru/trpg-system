「妖術」と呼ばれる独自の魔法を操ります。
&br
「妖術」の中でも妖を呼び出して自らに憑依させる「口寄せ」はかなり危険ですが、とても強力な術となります。
&br
アパリシャーは町の中での結界維持が最も重要な役割であるガーディナーと異なり、結界の外での妖退治が最も重要な役割となります。
そのためアパリシャーのほとんどが冒険者となります。
&br
ガーディナーほど社会的地位は高くありませんが、妖に対抗するための重要な戦力であることは確かであり、昔からシナノシマで活躍しているため、偏見や差別はほとんどありません。

