&2 儀式魔法と儀式判定
陰陽術には儀式魔法と呼ばれる特殊な魔法がいくつか存在します。
&br
儀式魔法には「儀式発動値」「継続発動値」「継続値」という3つの特殊な値があります。

&3 儀式の用意
儀式魔法を使う際、まず「儀式」を用意する必要があります。
「儀式」を用意できるのは儀式魔法である陰陽術を使用可能なキャラクターのみです。
「儀式」を用意することは補助動作で行うことができます。
&br
「儀式」は「儀式達成値」という値を持ちます。初期値は0です。
&br
「儀式」は陣営ごとに1つまでで、新たに「儀式」を用意すると前あった「儀式」はなくなります。
この際、前あった「儀式」の「儀式達成値」は引き継がれません。

&3 儀式判定と儀式達成値
「儀式」が用意されている陣営のキャラクターは、その儀式魔法の定めるMPを消費することで「儀式判定」を行うことができます。
「儀式判定」を行う際、ガーディナー技能は必須ではありません。
&br
「儀式判定」を行った場合、達成値を「儀式」の「儀式達成値」に足します。

&3 儀式達成値の持ち越し
「儀式達成値」は基本的に減ることはなく、ラウンドが変わっても持ち越されます。
ただし、ラウンドの終了時、このラウンドでの「儀式達成値」の上昇分が儀式魔法の「継続値」を下回っている場合、「儀式達成値」は0となってしまいます。

&3 儀式魔法の発動
「儀式達成値」が「儀式発動値」を初めて超えたとき、儀式魔法が発動します。
儀式魔法の対象の決定は発動する際に、儀式を用意したキャラクターが選びます。

&3 儀式魔法の継続発動
また、「儀式発動値」を超えたあとは、そのラウンドで初めて、このラウンドでの「儀式達成値」の上昇分が「継続発動値」を超えた場合、儀式魔法が発動します。
儀式魔法が発動する回数は、たとえキャラクターが何人「儀式判定」を行ったとしても、陣営ごと、ラウンドごとに1回だけです。
&br
「儀式達成値」が「儀式発動値」を超えた後であっても、ラウンドの終了時、このラウンドでの「儀式達成値」の上昇分が儀式魔法の「継続値」を下回っている場合、「儀式達成値」は0となってしまいます。

