&2 妖術
妖術はアパリシャー技能を習得することで使用できる様になる魔法です。
原理的には妖が妖精と近いことから妖精魔法に近いとされますが、一方で敵対するものの力を強引に支配して使う点でデーモンルーラーの招異と近いともいわれます。

&2 口寄せ
妖術「口寄せ」を使用すると自らに妖を憑依させることができます。
この状態を「口寄せ状態」と呼びます。
口寄せ状態では憑依する妖にごとに特殊な能力を得たり特定の能力値が上昇したりします。これは「妖能力」と呼ばれます。
&br
口寄せによって憑依する妖は基本的にランダムに決まりますが、特定の妖を口寄せすることが可能になる妖術もあります。

&3 口寄せできる妖
口寄せできる妖は「口寄せデータ」を持っています。
&br
口寄せデータには「第一抵抗目標値」「第二抵抗目標値」「妖能力」があります。

&3 抵抗
口寄せ状態の時、自分の手番の開始時に精神抵抗判定を行います。
&br
達成値が妖の第二抵抗目標値以下だった場合、HPに（妖の魔物レベル）点の確定ダメージを受けます。
さらに達成値が妖の第一抵抗目標値以下だったなら、その手番は強制的に終了します。

&3 口寄せの終了
口寄せ状態の時、1ラウンドに1回だけ補助動作で「口寄せの終了」を試みることができます。
精神抵抗判定を行い、達成値が妖の第二抵抗目標値以下だった場合、口寄せを終了することができます。
&br
この際、精神抵抗判定が自動成功の場合を除き、HPに（妖の魔物レベル）点の確定ダメージを受けます。

