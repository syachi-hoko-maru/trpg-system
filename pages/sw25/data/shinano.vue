&1 新種族解説：クローリー
シナノシマ以外ではほとんど見られない、ほぼ固有の種族がこのクロウリーです。
頭部が鳥の姿をしており、背中には翼が生え、手足も鳥の様です。蛮族であるガルーダに酷似しており、シナノシマの外から来た人からは警戒されますが、歴とした穢れを持たない人族です。
体長は男女ともに1.8mほどで、ガルーダよりも小柄です。
&br
卵生で15歳で成人として扱われます。寿命については、前述の通りシナノシマではほとんどが寿命を全うすることなく命を落とすため詳しくはわかっていませんが、100年程度だと考えられています。
&br
出自については謎が多いですが、シナノシマでは一般的にカタミナスによって力を与えられた人間、もしくは鳥の子孫だと考えられています。
また、リルドラケンと近い種族と考えられることもあり、リルドラケンから派生したのではないかとも言われます。
&br
上述のガルーダやリルドラケンと異なり、筋力が低く、器用度も高くないため、戦士には向きません。
これをクローリーたちは、筋力が自慢であったカタミナスがティダンに負けたことで筋力以外に注目して先祖に加護を与えたからだと考えています。
&br
一方で敏捷度・知力・精神力が高いため、ガーディナー技能やアパリシャー技能、スカウト技能やレンジャー技能に適性があります。

