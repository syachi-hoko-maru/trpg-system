&2 種族と生まれで決まるもの
ルールブック通りの順番でキャラクターを作る場合、まずは「種族」、次に「生まれ」を決めることになります。
&br
「種族」を選ぶことで、「種族特徴」が決まり、またおおよその能力値の傾向が決まります。
「生まれ」を選ぶことで、能力値と初期技能が1〜2つ決まります。

&3 種族特徴
種族特徴とは、その名の通り種族の特徴を表すものです。
&br
使いやすい種族特徴もあれば、使いにくいものもあります。
&br
初心者の方に意識してほしいのは、 &em_種族特徴を活かさないといけないわけではない ということです。
&br
ナイトメアなら魔法戦士をしないといけないというわけではありません。
純粋な戦士をしても、魔法使いをしても、もちろん他のことをしても、いいんです。
&br
「種族特徴を活かさなきゃ……」とは思いすぎないようにしましょう。

&2 能力値
「生まれ」によって決まる心技体の数値と種族ごとのA〜Fの数値を足すことで能力値が決まります。
&br
種族ごとのA〜Fの数値はダイスを振って決めますが、多くの場合「3パターン作ってどれかを選ぶ」という方法を取ります。
このどれを選ぶかは難しいところです。
&br
この際に意識すると良いことは &em_「必要な能力値を理解する」 ことと &em_「6の倍数にする」 ことです。

&3 必要な能力値を理解する
必要な能力値は使う技能などによって変わってきます。
タイプ別に解説するのでこのページの下から飛んでください。

