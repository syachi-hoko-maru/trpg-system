&3 近接タイプ
近接タイプは「ファイター」「グラップラー」「フェンサー」「バトルダンサー」のいずれかの技能をとって、敵の攻撃をかわしつつ近接攻撃をする型です。
&br
基本的に前衛になるのはこのタイプになります。「前衛になれるキャラクターが半分以上いる」の条件を満たすため、パーティーの半分は近接タイプにするのが良いでしょう。
&br
近接型にも純粋にダメージを求めるアタッカーだけでなく、手数を増やして戦うタイプ・回避力を高くして攻撃をかわすことを優先するタイプ・防護点を高くして受けるダメージを減らすタイプなどがいます。
戦闘特技《かばう》を使って味方への攻撃を肩代わりするタイプもいるでしょう。
&br
またこのウェブサイトでは魔法使い技能と上記4技能のいずれかを両立する「魔法戦士」も近接型に含めます。

&3 遠隔アタッカータイプ
